#include "minitalk.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	chr;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		n *= -1;
		write(fd, "-", 1);
	}
	chr = n % 10 + '0';
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &chr, 1);
}
